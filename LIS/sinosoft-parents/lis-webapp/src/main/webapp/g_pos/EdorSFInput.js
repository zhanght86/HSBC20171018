//查询收付费数据
function queryGetInfo() {
	
	tSQLInfo = new SqlClass();
	tSQLInfo.setResourceName("g_pos.EdorSFSql");
	tSQLInfo.setSqlId("EdorSFSql1");
	tSQLInfo.addSubPara(tEdorNo);
	tSQLInfo.addSubPara(tEdorType);
	
	var arrRuselt = easyExecSql(tSQLInfo.getString(), 1, 0, 1);
	
	fm.GetType.value=arrRuselt[0][0];
	fm.GetTypeName.value=arrRuselt[0][1];
	fm.GetMoney.value=arrRuselt[0][2];
}

//保存
function saveClick(){
	
	if (!verifyInput2()) {
		return false;
	}
	
	mOperate="SAVE";
	fm.action = "./EdorSFSave.jsp?Operate="+ mOperate+"&EdorType="+tEdorType+"&EdorNo="+tEdorNo+"&EdorAppNo="+tEdorAppNo+"&GrpContNo="+tGrpContNo+"&MissionID="+tMissionID+"&SubMissionID="+tSubMissionID+"&ActivityID="+tActivityID;
	submitFunc();
}

