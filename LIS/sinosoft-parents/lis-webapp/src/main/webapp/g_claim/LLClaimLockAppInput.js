/**
	*解锁申请
	*/
function appUnlock(){
	
	var i = LockGrid.getSelNo();
   if(i == 0 ) {
   		alert("请先选择一条记录！");
   }else {	
   	   	
   	fm.LockNo.value = LockGrid.getRowColData(i-1,1);
   	fm.Operate.value="APPUNLOCK";
   	submitForm();
	}
}

