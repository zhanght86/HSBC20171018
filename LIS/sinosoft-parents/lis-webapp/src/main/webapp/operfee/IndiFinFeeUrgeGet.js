function checkDate()
{
  if(!isDate(document.all('StartDate').value)||!isDate(document.all('EndDate').value))  
    {
      alert("日期输入有误!");
      return false;
    }
  var flag=compareDate(document.all('StartDate').value,document.all('EndDate').value);
  if(flag==1)
    {
       alert("日期输入有误!");
       return false;
    }
  return true;		
}

