/**
 * 提交前的校验、计算
 */
function beforeSubmit() {
	
	//系统的校验方法
	if (!verifyInput2()) {
		return false;
	}
	
	if (mOperate=="INSERT") {
		
		if (fm.InvestDesc.value.length>250) {
			alert("调查描述内容过长！");
			return false;
		}
		
		if (fm.DeathFlag.value=="0") {
			
			if (fm.DeathDate.value!="") {
				alert("身故日期不允许录入！");
				fm.DeathDate.value = "";
				return false;
			}
		}	else if (fm.DeathFlag.value=="1") {
			
			if (fm.DeathDate.value=="") {
				alert("身故日期必须录入！");
				return false;
			}
		}
		
		if (fm.DeathDate.value!="" && fm.DeathDate.value>tCurrenDate) {
				alert("身故日期必须小于等于当前日期！");
				return false;
		}
	}
	
	return true;
}