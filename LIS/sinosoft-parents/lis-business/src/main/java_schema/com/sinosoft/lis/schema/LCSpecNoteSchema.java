private static Logger logger = Logger.getLogger(LCSpecNoteSchema.class);

	// @Field
	/** 保单号码 */
	private String PolNo;
	/** 批注顺序 */
	private int NoteOrder;
	/** 批注内容 */
	private String NoteContente;
	/** 操作员 */
